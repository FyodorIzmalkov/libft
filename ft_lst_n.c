#include "libft.h"
#include <stdlib.h>

t_list	*ft_lst_n(t_list *lst, size_t n)
{
	size_t i;

	i = 0;
	while (i < n && lst)
	{
		lst = lst->next;
		i++;
	}
	return (lst);
}
